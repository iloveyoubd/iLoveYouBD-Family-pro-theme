    $logs = get_option('ily_autopilot_logs', []);
    $log_entry = [
        'post_id' => $res['post_id'],
        'title' => $res['title'],
        'author' => $res['author'],
        'category' => $res['category'],
        'timestamp' => current_time('d M, Y h:i A') . ' (BDT)',
        'view_url'   => $res['view_url'],
        'edit_url'   => $res['edit_url']
    ];
    array_unshift($logs, $log_entry);
    update_option('ily_autopilot_logs', array_slice($logs, 0, 50));
    
    wp_send_json_success([
        'post_id' => $res['post_id'],
        'title' => $res['title'],
        'view_url' => $res['view_url'],
        'edit_url' => $res['edit_url'],
        'author' => $res['author']
    ]);
}

/**
 * Handle 1-click single comment auto reply triggers
 */
function ily_auto_reply_comment_now_ajax_handler() {
    if (!current_user_can('manage_options')) {
        wp_send_json_error(['message' => 'অননুমোদিত অ্যাক্সেস।']);
    }
    $comment_id = intval($_POST['comment_id'] ?? 0);
    if ($comment_id <= 0) {
        wp_send_json_error(['message' => 'অবৈধ কমেন্ট আইডি।']);
    }
    
    $res = ily_reply_to_comment_via_ai($comment_id);
    if (is_wp_error($res)) {
        wp_send_json_error(['message' => $res->get_error_message()]);
    }
    
    wp_send_json_success($res);
}
