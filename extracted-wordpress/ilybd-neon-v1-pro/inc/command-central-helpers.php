/**
 * Get Top-Level Unreplied Comments on Autogenerated AI Posts
 */
function ily_get_unreplied_ai_comments() {
    $logs = get_option('ily_autopilot_logs', []);
    if (empty($logs)) {
        return [];
    }
    $post_ids = [];
    foreach ($logs as $log) {
        if (!empty($log['post_id'])) {
            $post_ids[] = intval($log['post_id']);
        }
    }
    if (empty($post_ids)) {
        return [];
    }
    
    $comments = get_comments([
        'post__in' => $post_ids,
        'status' => 'approve',
        'parent' => 0, // Top-level comments only
    ]);
    
    $unreplied = [];
    if (is_array($comments)) {
        foreach ($comments as $comment) {
            $replies = get_comments([
                'parent' => $comment->comment_ID,
                'count' => true
            ]);
            if ($replies == 0) {
                $unreplied[] = $comment;
            }
        }
    }
    return $unreplied;
}

/**
 * Generate Reply to Comment via Gemini and Post it back
 */
function ily_reply_to_comment_via_ai($comment_id) {
    if (!$comment_id) {
        return new WP_Error('invalid_comment', 'Invalid Comment ID');
    }
    $comment = get_comment($comment_id);
    if (!$comment) {
        return new WP_Error('invalid_comment', 'Comment reference not found');
    }
    
    $replies = get_comments([
        'parent' => $comment->comment_ID,
        'count' => true
    ]);
    if ($replies > 0) {
        return new WP_Error('already_replied', 'Comment already has automated replies');
    }
    
    $post = get_post($comment->comment_post_ID);
    if (!$post) {
        return new WP_Error('invalid_post', 'Post context not found');
    }
    
    $author_id = $post->post_author;
    $author_name = get_the_author_meta('display_name', $author_id) ?: 'Admin';
    $author_bio = get_the_author_meta('description', $author_id) ?: 'আই লাভ ইউ বিডি প্রযুক্তি কন্টেন্ট ক্রিয়েটর।';
    
    $admin_keys_raw = get_option('cyber_bot_api_keys');
    if (empty($admin_keys_raw)) {
        $admin_keys_raw = "AIzaSyBAcwAPXPzNfeGQ6XHDR-EaNRsHqhkTro8"; // fallback
    }
    $api_keys = array_values(array_filter(array_map('trim', explode("\n", $admin_keys_raw))));
    if (empty($api_keys)) {
        return new WP_Error('no_keys', 'Rotated API Keys are missing or empty');
    }
    
    $prompt = "You are " . $author_name . ", the author of the post titled: '" . $post->post_title . "'.
    The post content snippet is: '" . wp_strip_all_tags(mb_substr($post->post_content, 0, 1000)) . "...'
    Your bio is: '" . $author_bio . "'
    
    A visitor named '" . $comment->comment_author . "' commented on this article:
    \"" . $comment->comment_content . "\"
    
    Write a highly professional, helpful, polite, and human-like comment reply in Bangla (with matching emoji context if appropriate).
    Keep it strictly around 1 to 3 soft sentences. Speak natively as a skilled technical writer. Do NOT use markdown headings or general template responses.";
    
    $system_instructions = "You are $author_name replying to a reader's comment in fluent Bangla.";
    $reply_text = ily_call_gemini_api_direct($api_keys, $prompt, 400, false, $system_instructions);
    
    if (is_wp_error($reply_text) || empty($reply_text)) {
        return new WP_Error('ai_failed', 'Gemini text logic could not generate reply');
    }
    
    $reply_text = trim($reply_text, " \t\n\r\0\x0B\"'*#·-");
    
    // Process insertions
    $time = current_time('mysql');
    $data = [
        'comment_post_ID' => $comment->comment_post_ID,
        'comment_author' => $author_name,
        'comment_author_email' => get_the_author_meta('user_email', $author_id) ?: 'sazzad@iloveyoubd.com',
        'comment_author_url' => home_url(),
        'comment_content' => $reply_text,
        'comment_type' => 'comment',
        'comment_parent' => $comment->comment_ID,
        'user_id' => $author_id,
        'comment_date' => $time,
        'comment_approved' => 1,
    ];
    
    $reply_id = wp_insert_comment($data);
    if (!$reply_id) {
        return new WP_Error('insert_failed', 'Could not write reply to WordPress database');
    }
    
    return [
        'reply_id' => $reply_id,
        'reply_text' => $reply_text,
        'author' => $author_name
    ];
}

