add_action('template_redirect', function() {
    $master_active = get_option('ilybd_tools_active_master', 1);
    if (!$master_active) return;

    $is_tool_request = false;
    $tool_slug = '';
    $cat_slug = '';
    $is_hub = false;

    // First check query vars (elegant, correct WordPress way)
    if (get_query_var('ily_tools_hub')) {
        $is_hub = true;
        $is_tool_request = true;
    } elseif (get_query_var('ily_tool_category')) {
        $cat_slug = sanitize_text_field(get_query_var('ily_tool_category'));
        $is_tool_request = true;
    } elseif (get_query_var('ily_tool')) {
        $tool_slug = sanitize_text_field(get_query_var('ily_tool'));
        $is_tool_request = true;
    }

    // Fallback URL parsing to ensure backward compatibility and fail-safe routing
    if (!$is_tool_request) {
        $request_uri = $_SERVER['REQUEST_URI'] ?? '';
        $path = parse_url($request_uri, PHP_URL_PATH);
        $path = trim($path, '/');
        $segments = explode('/', $path);
        
        if (!empty($segments) && strtolower($segments[0]) === 'tools') {
            if (count($segments) === 1) {
                $is_hub = true;
                $is_tool_request = true;
            } elseif (count($segments) === 3 && strtolower($segments[1]) === 'category') {
                $cat_slug = sanitize_text_field($segments[2]);
                $is_tool_request = true;
            } elseif (count($segments) === 2) {
                $tool_slug = sanitize_text_field($segments[1]);
                $is_tool_request = true;
            }
        }
    }

    if (!$is_tool_request) return;

    // Handle AI Maya Assistant Short Links Redirection
    $lower_tool = strtolower($tool_slug);
    $maya_keywords = [
        'maya', 'ai-maya', 'bangla-ai', 'chatbot', 'ai-chatbot-bangla', 'chat-gpt-bangla',
        'এআই-মায়া', 'এআই-মায়া', 'মায়া-এআই', 'মায়া-এআই', 'মায়া', 'মায়া', 'বাংলা-এআই', 'বাংলা-চ্যাটবট'
    ];
    if (in_array($lower_tool, $maya_keywords)) {
        wp_redirect(home_url('/tools/ai-maya-cloud-assistant/'), 301);
        exit;
    }

    // Force 200 OK Status Header and Reset 404 in WordPress Query Engine
    status_header(200);
    global $wp_query;
    if (isset($wp_query)) {
        $wp_query->is_404 = false;
        $wp_query->is_home = false;
    }

    // Route 1: Main Tools Hub
    if ($is_hub) {
        ilybd_render_tools_hub_view();
        exit;
    }

    // Route 2: Category Filter
    if (!empty($cat_slug)) {
        $categories = ilybd_get_tools_categories();
        if (isset($categories[$cat_slug])) {
            ilybd_render_tools_category_view($cat_slug);
            exit;
        }
    }

    // Route 3: Single Tool Page
    if (!empty($tool_slug)) {
        if (ilybd_is_valid_tool($tool_slug)) {
            ilybd_increment_tool_stat($tool_slug, 'views');
            ilybd_render_single_tool_view($tool_slug);
            exit;
        }
    }
}, 5);
