/* =========================================================================
   5. TEMPLATE ROUTING & CAPTURING CONTROLLER (URL PARSER TO BYPASS DB LOADS)
   ========================================================================= */
add_action('template_redirect', function() {
    $master_active = get_option('ilybd_tools_active_master', 1);
    if (!$master_active) return;

    $request_uri = $_SERVER['REQUEST_URI'] ?? '';
    $path = parse_url($request_uri, PHP_URL_PATH);
    $path = trim($path, '/');
    
    // Check if route matches tools paradigm
    $segments = explode('/', $path);
    if (empty($segments) || strtolower($segments[0]) !== 'tools') return;
    
    // Route 1: Main Tools Hub (/tools/ or /tools)
    if (count($segments) === 1) {
        ilybd_render_tools_hub_view();
        exit;
    }
    
    // Route 2: Category Filter (/tools/category/{slug})
    if (count($segments) === 3 && strtolower($segments[1]) === 'category') {
        $cat_slug = sanitize_text_field($segments[2]);
        $categories = ilybd_get_tools_categories();
        if (isset($categories[$cat_slug])) {
            ilybd_render_tools_category_view($cat_slug);
            exit;
        }
    }
    
    // Route 3: Single Tool Page (/tools/{tool-slug})
    if (count($segments) === 2) {
        $tool_slug = sanitize_text_field($segments[1]);
        if (ilybd_is_valid_tool($tool_slug)) {
            ilybd_increment_tool_stat($tool_slug, 'views');
            ilybd_render_single_tool_view($tool_slug);
            exit;
        }
    }
}, 5);
