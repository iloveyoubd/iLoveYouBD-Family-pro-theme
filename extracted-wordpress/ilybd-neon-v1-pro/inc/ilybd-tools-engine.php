function ilybd_get_tool_stat($slug, $metric) {
    // metrics: 'views', 'usages', 'likes', 'favs'
    return (int) get_option("ilybd_tool_{$slug}_{$metric}", 0);
}

function ilybd_increment_tool_stat($slug, $metric) {
    $val = ilybd_get_tool_stat($slug, $metric);
    $val++;
    update_option("ilybd_tool_{$slug}_{$metric}", $val);
    return $val;
}

