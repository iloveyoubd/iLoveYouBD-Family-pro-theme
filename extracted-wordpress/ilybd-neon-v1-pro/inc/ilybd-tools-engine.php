// Register custom rewrite rules for dynamic tools to prevent 404 errors and ensure 100% SEO indexing compatibility
add_action('init', 'ilybd_tools_add_custom_rewrite_rules', 10);
function ilybd_tools_add_custom_rewrite_rules() {
    add_rewrite_rule('^tools/?$', 'index.php?ily_tools_hub=1', 'top');
    add_rewrite_rule('^tools/category/([^/]+)/?$', 'index.php?ily_tool_category=$matches[1]', 'top');
    add_rewrite_rule('^tools/([^/]+)/?$', 'index.php?ily_tool=$matches[1]', 'top');
    
    // Auto-flush rewrite rules once to ensure they are active immediately
    if (!get_option('ilybd_tools_rules_flushed_v2')) {
        flush_rewrite_rules(false);
        update_option('ilybd_tools_rules_flushed_v2', 1);
    }
}

